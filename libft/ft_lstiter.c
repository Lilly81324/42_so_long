#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*node;

	node = lst;
	if (lst == NULL)
		return ;
	while (node != NULL)
	{
		f(node->content);
		node = node->next;
	}
}
